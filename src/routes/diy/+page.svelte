	<h2 class="gradient-heading">Why We Ink Well</h2>
	<blockquote>
		<p class="text-surface-700-200-token">
			Deep within us there sits a well. A well without a bottom, the well of being, some say. Diving
			in, we begin to fathom the Source from whence raw love bubbles forth. A foamy froth forms on
			the surface. With our two tools, attention and intent, we shape the foam of love. When the
			foam is in-formation, we are ready to share our creation. We share the love we shape through
			words and deeds, movement and speech. But the love we seek to share often comes with a taint.
			We witness our best intentions mangled and stained by some unseen force. For in that well of
			being, our shadow pools unseen, trickling down behind the mind's eye. The inky syrup of fear
			lurks murky, coating love's bubbles in image and appearance, film and scum. To bring purer
			forms of love into the world, we must first draw out our shadow. May as well use the ink well.
		</p>
